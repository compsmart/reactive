// Generate slug from location name
export function locationToSlug(location: string): string {
  return location.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
}

// Get location metadata for SEO
export function getLocationMeta(location: string) {
  const slug = locationToSlug(location);
  return {
    slug,
    title: `Find Trusted Contractors in ${location} | Reactive`,
    description: `Connect with verified local tradespeople in ${location}. Get free quotes from plumbers, electricians, builders & more. Join the Reactive Network today.`,
    keywords: `contractors ${location}, tradespeople ${location}, plumber ${location}, electrician ${location}, builder ${location}`,
  };
}

